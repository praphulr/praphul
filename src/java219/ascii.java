package java219;
public class ascii   
{  
    public static void main(String[] args)   
    {  
        char chr1 = 'p';  
        char chr2 = 's';  

        int ascii1 = chr1;  
        int ascii2 = chr2;  
        
        System.out.println("The ASCII value of " + chr1 + " : " + ascii1);  
        System.out.println("The ASCII value of " + chr2 + " : " + ascii2);  
    }  
}  